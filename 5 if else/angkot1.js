var jumlahAngkot = 10;
var angkotSehat = 6;

for (noAngkot = 1; noAngkot <= jumlahAngkot; noAngkot++)
{
    if (noAngkot <= angkotSehat)
    {
        console.log('angkot No.' + noAngkot + ' beroperasi dengan baik')
    }
    else
    {
        console.log('angkot No.' + noAngkot + ' tidak beroperasi dengan baik');
    }
}